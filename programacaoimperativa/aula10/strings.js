let texto = "Lucas Fontenele dos Santos"

// lenght
console.log(texto.length);
// Conta quantos componentes possuem na string citada. Vale ressaltar que os espaços são contabilizados.

//indexOf
console.log(texto.indexOf("Fontenele"));
// Informa em qual componente o objeto começa dentro da string.

//slice
console.log(texto.slice(0,5));
// Informa o campo da string que eu coloquei dentro do parâmetro.

//trim
console.log(texto.trim());
//Remover espaços antes e depois da ocorrência de valores alfanuméricos.

//split
console.log(texto.split(" "));
//Retorna os textos como array.

//replace
console.log(texto.replace("Fontenele","Lindo"));
//Recebe no primeiro parâmetro o objetivo identificado e no segundo ele substitui pelo termo citado. 

